fun main() {
    // Do While Loop
    // perulangan yang bisa dibilang serupa dengan while loop
    // perbedaanya pada do while loop, kode blok akan dijalankan terlebih dahulu,
    // baru diakhir dilakukan pengecekan kondisi.

    var i = 0;
    do {
        println("$i - Do While Loop")
        i++
    }while (i<10)

    println("")
    var j = 0;
    do {
        println("$j - Do While Loop")
        j++
    }while (j>10)
}